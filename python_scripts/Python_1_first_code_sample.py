#-----simple list loop-----
# This script demonstrates the difference between multiplying a whole list (repeating its elements) and multiplying individual numbers in the list.

Numbers = [1,5,10]

for nun in Numbers:
    print(Numbers * 2)

Numbers = [1,5,10]

for nun in Numbers:
    print(nun * 2)